package com.juniter.model;

public class Sample {
	private int id;
	private int range;

	public int getId() {
		return id;
	}

	public Sample setId(int id) {
		this.id = id;
		return this;
	}

	public int getRange() {
		return range;
	}

	public Sample setRange(int range) {
		this.range = range;
		return this;
	}

}
